    private void btnTambah1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambah1ActionPerformed
        String npm = (String) comboNPM.getSelectedItem(); // Ambil NPM dari ComboBox
        String kodeMK = txtKodeMK.getText();
        String namaMK = txtNamaMK.getText();
        String sks = txtSKS.getText();
        String semester = txtSemester.getText();
        String tahunAkademik = txtTahunAkademik.getText();
        String namaDosen = txtNamaDosen.getText();

        if (npm == null || npm.isEmpty() || kodeMK.isEmpty() || namaMK.isEmpty() || sks.isEmpty()
                || semester.isEmpty() || tahunAkademik.isEmpty() || namaDosen.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
            return;
        }

        try (Connection conn = getConnection()) {
            String sql = "INSERT INTO pengambilan_krs (npm, kode_mata_kuliah, nama_mata_kuliah, sks, semester, tahun_akademik, nama_dosen) "
                    + "VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, npm);
            pst.setString(2, kodeMK);
            pst.setString(3, namaMK);
            pst.setInt(4, Integer.parseInt(sks));
            pst.setString(5, semester);
            pst.setInt(6, Integer.parseInt(tahunAkademik));
            pst.setString(7, namaDosen);

            int inserted = pst.executeUpdate();
            if (inserted > 0) {
                JOptionPane.showMessageDialog(this, "Data KRS berhasil disimpan.");
                loadKRSTable(); // Refresh tabel KRS
            } else {
                JOptionPane.showMessageDialog(this, "Data KRS gagal disimpan.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Format angka salah: " + e.getMessage());
        }
    }//GEN-LAST:event_btnTambah1ActionPerformed

