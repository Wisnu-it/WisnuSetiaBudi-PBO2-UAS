    private void btnHapus1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapus1ActionPerformed
        // Ambil baris yang dipilih di tabel
        int selectedRow = tableKRS.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus.");
            return;
        }

        // Ambil id_pengambilan dari tabel
        String idPengambilan = tableKRS.getValueAt(selectedRow, 0).toString();

        int confirm = JOptionPane.showConfirmDialog(this,
                "Apakah Anda yakin ingin menghapus data ini?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try (Connection conn = getConnection()) {
                String sql = "DELETE FROM pengambilan_krs WHERE id_pengambilan = ?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, idPengambilan);

                int deleted = pst.executeUpdate();
                if (deleted > 0) {
                    JOptionPane.showMessageDialog(this, "Data KRS berhasil dihapus.");
                    loadKRSTable(); // Refresh tabel KRS
                } else {
                    JOptionPane.showMessageDialog(this, "Data KRS gagal dihapus.");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapus1ActionPerformed

